#include "libft.h"

int	ft_atoi(char *str, int *idx)
{
	int	nbr;
	int	i;

	nbr = 0;
	i = *idx;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr *= 10;
		nbr += str[i] - '0';
		i++;
	}
	*idx = i;
	return (nbr);
}
